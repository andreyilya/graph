public class ContentXpath {
    private String headerXpath;
    private String titleXpath;
    private String descriptionXpath;
    private String keywordsXpath;
    private String contentXpath;

    public String getHeaderXpath() {
        return headerXpath;
    }

    public void setHeaderXpath(String headerXpath) {
        this.headerXpath = headerXpath;
    }

    public String getTitleXpath() {
        return titleXpath;
    }

    public void setTitleXpath(String titleXpath) {
        this.titleXpath = titleXpath;
    }

    public String getDescriptionXpath() {
        return descriptionXpath;
    }

    public void setDescriptionXpath(String descriptionXpath) {
        this.descriptionXpath = descriptionXpath;
    }

    public String getKeywordsXpath() {
        return keywordsXpath;
    }

    public void setKeywordsXpath(String keywordsXpath) {
        this.keywordsXpath = keywordsXpath;
    }

    public String getContentXpath() {
        return contentXpath;
    }

    public void setContentXpath(String contentXpath) {
        this.contentXpath = contentXpath;
    }
}
